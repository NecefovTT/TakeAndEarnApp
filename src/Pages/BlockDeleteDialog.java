    public BlockDeleteDialog(java.awt.Frame parent, boolean modal, int id, String keyword) {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(this);

        this.id = id;
        this.keyword = keyword;

        if (keyword == "Block") {
            u = dbc.findByid(id);
            lblKeyWord.setText(u.getName() + " " + u.getSurname() + " " + "bloklansınmı ?");
        }
        if (keyword == "UnBlock") {
            u = dbc.findByid(id);
            lblKeyWord.setText(u.getName() + " " + u.getSurname() + " " + "blokdan cıxarılsınmı ?");
        }
        if (keyword == "DeleteEquip") {
            lblKeyWord.setText("Seçdiyiniz vasitə bloklansınmı ?");
        }
        if (keyword == "UnDeleteEquip") {
            lblKeyWord.setText("Seçdiyiniz vasitə blokdan çıxarılsınmı ?");
        }
        if (keyword == "DeleteTheRoute") {
            lblKeyWord.setText("Seçdiyiniz sifariş silinsinmi ?");
        }

    }

