    private void btnYesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnYesActionPerformed
        if (keyword == "Block") {
            dbc.blockUsers(id);
            dispose();
        }
        if (keyword == "UnBlock") {
            dbc.unblockUsers(id);
            dispose();
        }
        if (keyword == "DeleteEquip") {
            dbc.blockEquipment(id);
            dispose();
        }
        if (keyword == "UnDeleteEquip") {
            dbc.unBlockEquipment(id);
            dispose();
        }
        if (keyword == "DeleteTheRoute") {
            dbc.deleteTheRoute(id);
            dispose();
        }

    }//GEN-LAST:event_btnYesActionPerformed

